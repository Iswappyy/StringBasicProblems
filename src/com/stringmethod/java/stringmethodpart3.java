package com.stringmethod.java;

public class stringmethodpart3 {
	public static void main(String[] args) {
		String s = "Swapnil";
		
		System.out.println(s.charAt(3));
		System.out.println(s.indexOf("ap"));
		System.out.println(s.lastIndexOf("p"));
		System.out.println(s.contains("pni"));
		System.out.println(s.startsWith("S"));
		System.out.println(s.endsWith("i"));
		System.out.println(s.toUpperCase());
		System.out.println(s.toLowerCase());
	}

}
